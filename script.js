// O cÃ³digo do quiz segue a estrutura jÃ¡ discutida, com a funÃ§Ã£o de exibir e verificar respostas.
let currentQuestionIndex = 0;

function loadQuestion() {
    const question = quizData.questions[currentQuestionIndex];
    document.getElementById('question-text').textContent = question.question;
    document.getElementById('question-image').src = question.image;

    const options = document.querySelectorAll('.option');
    options.forEach((option, index) => {
        option.textContent = question.options[index];
    });

    document.getElementById('result-container').style.display = 'none';
}

function checkAnswer(selectedIndex) {
    const question = quizData.questions[currentQuestionIndex];
    const resultText = document.getElementById('result-text');
    if (selectedIndex === question.correctAnswer) {
        resultText.textContent = "Resposta correta!";
    } else {
        resultText.textContent = "Resposta errada!";
    }

    document.getElementById('result-container').style.display = 'block';
}

function nextQuestion() {
    currentQuestionIndex++;
    if (currentQuestionIndex < quizData.questions.length) {
        loadQuestion();
    } else {
        alert("VocÃª completou o quiz!");
    }
}

